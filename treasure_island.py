  swim_wait = input("Would you like to swim or wait? \n")
  swim_wait_lower = swim_wait.lower()
  if swim_wait_lower == "swim":
    print("You have been attacked by a trout. \nGAME OVER.")
  
  else:
    door = input("Which colour door would you like to enter? \n")
    door_lower = door.lower()
    if door_lower == "red":
      print("Burned by fire. \nGAME OVER.")
    elif door_lower == "yellow":
      print("Congratulations, YOU WIN.")
    elif door_lower == "blue":
      print("Eaten by beasts. \nGAME OVER.")
    else:
      print("GAME OVER")
